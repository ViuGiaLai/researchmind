def test_docx(output_dir="."):
    """Test DOCX export using python-docx + shared parser"""
    try:
        from docx import Document
        from docx.shared import Pt, Cm, RGBColor
        from docx.enum.text import WD_ALIGN_PARAGRAPH
        from docx.oxml.ns import qn
        from docx.oxml import OxmlElement
        from export import _parse_md_events, _add_formatted_text

        doc = Document()
        style = doc.styles["Normal"]
        style.font.name = "Times New Roman"
        style.font.size = Pt(12)
        style.paragraph_format.line_spacing = 1.5
        style.paragraph_format.space_after = Pt(6)

        title_para = doc.add_heading("Research Synthesis Report", level=1)
        title_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        doc.add_paragraph()

        def _code_block(code_lines, lang):
            if lang:
                lp = doc.add_paragraph()
                lr = lp.add_run(lang)
                lr.font.size = Pt(8)
                lr.font.color.rgb = RGBColor(0x94, 0xa3, 0xb8)
                lr.italic = True
                lp.paragraph_format.space_after = Pt(2)
                lp.paragraph_format.space_before = Pt(4)
            for cl in code_lines:
                cp = doc.add_paragraph()
                cr = cp.add_run(cl.replace("\t", "    ") if cl else " ")
                cr.font.name = "Consolas"
                cr.font.size = Pt(8.5)
                cp.paragraph_format.space_after = Pt(0)
                cp.paragraph_format.space_before = Pt(0)
                cp.paragraph_format.line_spacing = 1.15
                shd = OxmlElement('w:shd')
                shd.set(qn('w:fill'), 'f1f5f9')
                shd.set(qn('w:val'), 'clear')
                cp.paragraph_format.element.get_or_add_pPr().append(shd)

        def _table(rows):
            if len(rows) < 2: return
            header_row = rows[0]
            data_rows = rows[2:] if len(rows) > 2 else []
            if not header_row: return
            table = doc.add_table(rows=1 + len(data_rows), cols=len(header_row))
            table.style = 'Table Grid'
            for ci, hcell in enumerate(header_row):
                cell = table.rows[0].cells[ci]
                cell.text = hcell.strip()
                for para in cell.paragraphs:
                    para.alignment = WD_ALIGN_PARAGRAPH.CENTER
                    for run in para.runs:
                        run.bold = True
                        run.font.color.rgb = RGBColor(0xFF, 0xFF, 0xFF)
                        run.font.size = Pt(10)
                shd = OxmlElement('w:shd')
                shd.set(qn('w:fill'), '6366f1')
                shd.set(qn('w:val'), 'clear')
                cell._tc.get_or_add_tcPr().append(shd)
            alt = ['f8fafc', 'ffffff']
            for ri, row in enumerate(data_rows):
                for ci, dcell in enumerate(row):
                    cell = table.rows[ri + 1].cells[ci]
                    cell.text = dcell.strip()
                    for para in cell.paragraphs:
                        para.alignment = WD_ALIGN_PARAGRAPH.CENTER
                        for run in para.runs:
                            run.font.size = Pt(10)
                    shd = OxmlElement('w:shd')
                    shd.set(qn('w:fill'), alt[ri % 2])
                    shd.set(qn('w:val'), 'clear')
                    cell._tc.get_or_add_tcPr().append(shd)
            doc.add_paragraph()

        for event, data in _parse_md_events(SAMPLE_MD):
            if event == "code_block": _code_block(data[0], data[1])
            elif event == "table": _table(data[0])
            elif event == "heading1": doc.add_heading(data[0], level=1)
            elif event == "heading2": doc.add_heading(data[0], level=2)
            elif event == "heading3": doc.add_heading(data[0], level=3)
            elif event == "hr":
                p = doc.add_paragraph()
                pPr = p.paragraph_format.element.get_or_add_pPr()
                pBdr = OxmlElement('w:pBdr')
                bottom = OxmlElement('w:bottom')
                bottom.set(qn('w:val'), 'single')
                bottom.set(qn('w:sz'), '6')
                bottom.set(qn('w:space'), '4')
                bottom.set(qn('w:color'), 'cbd5e1')
                pBdr.append(bottom)
                pPr.append(pBdr)
            elif event == "bullet_list":
                for item in data[0]:
                    p = doc.add_paragraph(style='List Bullet')
                    _add_formatted_text(p, item)
            elif event == "numbered_list":
                for item in data[0]:
                    p = doc.add_paragraph(style='List Number')
                    _add_formatted_text(p, item)
            elif event == "blockquote":
                for line in data[0]:
                    p = doc.add_paragraph(style='Quote')
                    _add_formatted_text(p, line)
            elif event == "paragraph":
                for line in data[0]:
                    p = doc.add_paragraph()
                    _add_formatted_text(p, line)

        doc.add_paragraph()
        fp = doc.add_paragraph()
        fp.alignment = WD_ALIGN_PARAGRAPH.CENTER
        fr = fp.add_run(f"Generated by ResearchMind VN on {datetime.now().strftime('%Y-%m-%d %H:%M')}")
        fr.font.size = Pt(9)
        fr.font.color.rgb = RGBColor(0x99, 0x99, 0x99)

        for section in doc.sections:
            section.top_margin = Cm(2.54)
            section.bottom_margin = Cm(2.54)
            section.left_margin = Cm(2.54)
            section.right_margin = Cm(2.54)

        path = os.path.join(output_dir, "test_all_exports.docx")
        doc.save(path)
        return True, path
    except Exception as e:
        return False, str(e)


