def test_pdf(output_dir="."):
    """Test PDF export using fpdf2 + shared parser"""
    try:
        from fpdf import FPDF
        from export import _parse_md_events

        pdf = FPDF()
        pdf.set_auto_page_break(auto=True, margin=20)

        # Cover page
        pdf.add_page()
        pdf.alias_nb_pages()
        pdf.ln(50)
        pdf.set_draw_color(99, 102, 241)
        pdf.set_line_width(1.2)
        mid_x = pdf.w / 2
        pdf.line(mid_x - 30, pdf.get_y(), mid_x + 30, pdf.get_y())
        pdf.ln(14)
        pdf.set_font("Helvetica", "B", 28)
        pdf.set_text_color(15, 23, 42)
        title_text = "Research Synthesis Report"
        pdf.multi_cell(0, 14, title_text, align="C")
        pdf.ln(6)
        pdf.set_font("Helvetica", "", 12)
        pdf.set_text_color(100, 116, 139)
        pdf.cell(0, 8, "Research Synthesis Report", align="C")
        pdf.ln(14)
        pdf.set_draw_color(99, 102, 241)
        pdf.set_line_width(1.2)
        pdf.line(mid_x - 30, pdf.get_y(), mid_x + 30, pdf.get_y())
        pdf.ln(40)
        pdf.set_font("Helvetica", "I", 9)
        pdf.set_text_color(148, 163, 184)
        pdf.cell(0, 6, "Generated by ResearchMind VN", align="C")
        pdf.ln(5)
        pdf.cell(0, 6, datetime.now().strftime('%B %d, %Y at %H:%M'), align="C")
        pdf.ln(5)
        pdf.cell(0, 6, "ResearchMind VN — AI-Powered Research Assistant", align="C")
        pdf.add_page()

        # Content pages
        pdf.set_text_color(30, 30, 30)

        def _strip(text):
            import re
            return re.sub(r"\*\*(.+?)\*\*", r"\1", re.sub(r"\*(.+?)\*", r"\1", text))

        def _code_block(code_lines, lang):
            line_h = 5
            x0 = pdf.l_margin
            y0 = pdf.get_y()
            code_w = pdf.w - pdf.l_margin - pdf.r_margin
            padding = 3
            lang_h = 4 if lang else 0
            xs = x0 + padding
            ys = y0 + padding
            total_lines = max(len(code_lines), 1)
            block_h = total_lines * line_h + 2 * padding + 2 + lang_h
            if y0 + block_h > pdf.h - 20:
                pdf.add_page()
                y0 = pdf.get_y()
                ys = y0 + padding
            pdf.set_fill_color(241, 245, 249)
            pdf.set_draw_color(203, 213, 225)
            pdf.rect(x0, y0, code_w, block_h, style="DF")
            if lang:
                pdf.set_font("Helvetica", "I", 7)
                pdf.set_text_color(148, 163, 184)
                pdf.set_xy(xs + 1, ys)
                pdf.cell(0, 4, lang)
                ys += lang_h
            pdf.set_font("Courier", "", 8)
            pdf.set_text_color(30, 41, 59)
            pdf.set_xy(xs, ys)
            for cl in code_lines:
                pdf.cell(0, line_h, cl.replace("\t", "    "))
                pdf.set_x(xs)
            pdf.set_y(ys + len(code_lines) * line_h + padding)
            pdf.ln(2)

        def _table(rows):
            if len(rows) < 2: return
            header = rows[0]
            data_rows = rows[2:] if len(rows) > 2 else []
            n_cols = len(header)
            if n_cols == 0: return
            col_w = (pdf.w - pdf.l_margin - pdf.r_margin) / n_cols
            ch = 7
            y0 = pdf.get_y()
            if y0 + (ch * (1 + len(data_rows)) + 2) > pdf.h - 20:
                pdf.add_page()
                y0 = pdf.get_y()
            x0 = pdf.l_margin
            pdf.set_font("Helvetica", "B", 9)
            pdf.set_fill_color(99, 102, 241)
            pdf.set_text_color(255, 255, 255)
            pdf.set_draw_color(99, 102, 241)
            pdf.set_xy(x0, y0)
            for hc in header:
                pdf.cell(col_w, ch, hc.strip(), border=1, fill=True, align="C")
            pdf.ln(ch)
            pdf.set_font("Helvetica", "", 9)
            pdf.set_text_color(30, 30, 30)
            pdf.set_draw_color(203, 213, 225)
            for ri, row in enumerate(data_rows):
                pdf.set_fill_color(248, 250, 252) if ri % 2 == 0 else pdf.set_fill_color(255, 255, 255)
                pdf.set_xy(x0, pdf.get_y())
                for dc in row:
                    pdf.cell(col_w, ch, dc.strip(), border=1, fill=True, align="C")
                pdf.ln(ch)
            pdf.ln(3)

        for event, data in _parse_md_events(SAMPLE_MD):
            if event == "code_block": _code_block(data[0], data[1])
            elif event == "table": _table(data[0])
            elif event == "heading1":
                pdf.set_font("Helvetica", "B", 16)
                pdf.set_text_color(15, 23, 42)
                pdf.multi_cell(0, 10, _strip(data[0]))
                pdf.ln(2)
            elif event == "heading2":
                pdf.set_font("Helvetica", "B", 13)
                pdf.set_text_color(30, 41, 59)
                pdf.multi_cell(0, 8, _strip(data[0]))
                pdf.ln(1)
            elif event == "heading3":
                pdf.set_font("Helvetica", "B", 11)
                pdf.set_text_color(51, 65, 85)
                pdf.multi_cell(0, 7, _strip(data[0]))
                pdf.ln(1)
            elif event == "hr":
                pdf.set_draw_color(200, 200, 200)
                pdf.set_line_width(0.3)
                pdf.line(pdf.l_margin, pdf.get_y() + 2, pdf.w - pdf.r_margin, pdf.get_y() + 2)
                pdf.ln(6)
            elif event == "bullet_list":
                for item in data[0]:
                    pdf.set_font("Helvetica", "", 10)
                    pdf.set_text_color(30, 30, 30)
                    pdf.cell(6, 6, chr(8226))
                    pdf.multi_cell(0, 6, _strip(item))
            elif event == "numbered_list":
                for idx, item in enumerate(data[0], 1):
                    pdf.set_font("Helvetica", "", 10)
                    pdf.set_text_color(30, 30, 30)
                    pdf.multi_cell(0, 6, f"{idx}. {_strip(item)}")
            elif event == "blockquote":
                for line in data[0]:
                    pdf.set_x(pdf.l_margin + 4)
                    pdf.set_font("Helvetica", "I", 10)
                    pdf.set_text_color(71, 85, 105)
                    ys = pdf.get_y()
                    pdf.multi_cell(pdf.w - pdf.l_margin - pdf.r_margin - 8, 6, line if line else " ")
                    pdf.set_draw_color(139, 92, 246)
                    pdf.set_line_width(0.8)
                    pdf.line(pdf.l_margin, ys, pdf.l_margin, pdf.get_y())
                    pdf.set_text_color(30, 30, 30)
            elif event == "paragraph":
                for line in data[0]:
                    pdf.set_font("Helvetica", "", 10)
                    pdf.set_text_color(30, 30, 30)
                    pdf.multi_cell(0, 6, _strip(line))

        def _footer(p):
            p.set_y(-15)
            p.set_font("Helvetica", "I", 8)
            p.set_text_color(150, 150, 150)
            p.cell(0, 10, f"Page {p.page_no()}/{{nb}}", align="C")
        pdf.footer = lambda: _footer(pdf)

        path = os.path.join(output_dir, "test_all_exports.pdf")
        pdf.output(path)
        return True, path
    except Exception as e:
        return False, str(e)


